package com.company.homework.Practice12;

public class Point {
    private double xVal;
    private double yVal;
    Point(double x, double y){
        xVal = x;
        yVal = y;
    }

    public double getxVal() {
        return xVal;
    }

    public void setxVal(double xVal) {
        this.xVal = xVal;
    }

    public double getyVal() {
        return yVal;
    }

    public void setyVal(double yVal) {
        this.yVal = yVal;
    }

}
